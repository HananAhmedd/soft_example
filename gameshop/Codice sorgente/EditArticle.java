    private void btnOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOKActionPerformed
        try
        {
            if (searchedArticle == null)
                searchedArticle = new Article();
            searchedArticle.setNomeArticolo(txtNome.getText());
            searchedArticle.setTipo(cmbTipo.getSelectedItem().toString());
            searchedArticle.setConsole(cmbConsole.getSelectedItem().toString());
            searchedArticle.setGenere(cmbGenere.getSelectedItem().toString().trim());
            searchedArticle.setDescrizione(txtDescrizione.getText());
            searchedArticle.Finalize();
            JOptionPane.showMessageDialog(null, "Operazione completata.");
        } catch (Exception ex) {JOptionPane.showMessageDialog(null, ex.getMessage());}
    }//GEN-LAST:event_btnOKActionPerformed


    public EditArticle(int codice) throws Exception
    {
        this(null, true);
        searchedArticle = new Article("Codice = " + codice);
        txtNome.setText(searchedArticle.getNomeArticolo());
        
        String tipo = searchedArticle.getTipo();
        if (tipo.equals("Console"))
            cmbTipo.setSelectedIndex(0);
        else if (tipo.equals("Accessorio"))
            cmbTipo.setSelectedIndex(1);
        else
            cmbTipo.setSelectedIndex(2);
        
        String console = searchedArticle.getConsole();
        if (console.equals("PC"))
            cmbConsole.setSelectedIndex(0);
        else if (console.equals("PS1"))
            cmbConsole.setSelectedIndex(1);
        else if (console.equals("PS2"))
            cmbConsole.setSelectedIndex(2);
        else if (console.equals("PS3"))
            cmbConsole.setSelectedIndex(3);
        else if (console.equals("PS4"))
            cmbConsole.setSelectedIndex(4);
        else if (console.equals("PSP"))
            cmbConsole.setSelectedIndex(5);
        else if (console.equals("PSVita"))
            cmbConsole.setSelectedIndex(6);
        else if (console.equals("Xbox"))
            cmbConsole.setSelectedIndex(7);
        else if (console.equals("Xbox 360"))
            cmbConsole.setSelectedIndex(8);
        else if (console.equals("Xbox One"))
            cmbConsole.setSelectedIndex(9);
        else if (console.equals("Wii"))
            cmbConsole.setSelectedIndex(10);
        else if (console.equals("WiiU"))
            cmbConsole.setSelectedIndex(11);
        else if (console.equals("NDS"))
            cmbConsole.setSelectedIndex(12);
        else if (console.equals("2DS"))
            cmbConsole.setSelectedIndex(13);
        else if (console.equals("3DS"))
            cmbConsole.setSelectedIndex(14);
        else if (console.equals("GBA"))
            cmbConsole.setSelectedIndex(15);
        else if (console.equals("GameCube"))
            cmbConsole.setSelectedIndex(16);
        else
            cmbConsole.setSelectedIndex(17);
        
        String genere = searchedArticle.getGenere();
        if (genere.isEmpty())
            cmbGenere.setSelectedIndex(0);
        else if (genere.equals("Avventura"))
            cmbGenere.setSelectedIndex(1);
        else if (genere.equals("Azione"))
            cmbGenere.setSelectedIndex(2);
        else if (genere.equals("GdR"))
            cmbGenere.setSelectedIndex(3);
        else if (genere.equals("Simulatore"))
            cmbGenere.setSelectedIndex(4);
        else if (genere.equals("Sport"))
            cmbGenere.setSelectedIndex(5);
        else if (genere.equals("Strategia"))
            cmbGenere.setSelectedIndex(6);
        else if (genere.equals("Indie"))
            cmbGenere.setSelectedIndex(7);
        else
            cmbGenere.setSelectedIndex(7);
        
        txtDescrizione.setText(searchedArticle.getDescrizione());        
    }
    

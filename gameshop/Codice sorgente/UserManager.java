    private void btnDatiUtenteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDatiUtenteActionPerformed
        Object id = jTable1.getModel().getValueAt(jTable1.getSelectedRow(), 0);
        try
        {
            new EditUser(Integer.parseInt(id.toString())).setVisible(true);
            loadUsers();
        } catch (Exception ex) {JOptionPane.showMessageDialog(null, ex.getMessage());}
    }//GEN-LAST:event_btnDatiUtenteActionPerformed

    private void btnDeregistraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeregistraActionPerformed
        try
        {
            if (jTable1.getSelectedRow() == -1) return;
            int yesNo = JOptionPane.showConfirmDialog(null, "Sei sicuro di voler deregistrare l'utente?", "Messaggio", JOptionPane.YES_NO_OPTION);
            if (yesNo != JOptionPane.YES_OPTION) return;
            Object id = jTable1.getModel().getValueAt(jTable1.getSelectedRow(), 0);
            User user = new User("ID = " + id + ";");
            user.Delete();
            JOptionPane.showMessageDialog(null, "Operazione completata.");
            loadUsers();
        } catch (Exception ex) {JOptionPane.showMessageDialog(null, ex.getMessage());}
    }//GEN-LAST:event_btnDeregistraActionPerformed


    private void loadUsers() throws SQLException, ClassNotFoundException, InstantiationException, IllegalAccessException 
    {
        Globals.getStatement().executeQuery("SELECT * FROM utenti;");
        Globals.updateRS();
        ((DefaultTableModel)jTable1.getModel()).getDataVector().removeAllElements();
        while (Globals.getResultSet().next())
        {
            ((DefaultTableModel)jTable1.getModel()).addRow(new Object[] {Globals.getResultSet().getString("ID"), Globals.getResultSet().getString("Nome").concat(" " + Globals.getResultSet().getString("Cognome")), Globals.getResultSet().getString("Ruolo")});
        }
    }
    
